void MyFrame::NotifyUsingFile(const wxString& name)
{
    wxString msg;
    msg << "Using sound file: " << name << "\n";
    m_tc->AppendText(msg);
}


// event handlers


void MyFrame::OnSelectFile(wxCommandEvent& WXUNUSED(event))
{
#if wxUSE_FILEDLG
    wxFileDialog dlg(this, "Choose a sound file",
                     wxEmptyString, wxEmptyString,
                     wxString::Format
                     (
                         "WAV files (*.wav)|*.wav|All files (%s)|%s",
                         wxFileSelectorDefaultWildcardStr,
                         wxFileSelectorDefaultWildcardStr
                     ),
                     wxFD_OPEN|wxFD_CHANGE_DIR);
    if ( dlg.ShowModal() == wxID_OK )
    {
        m_soundFile = dlg.GetPath();
#ifdef __WXMSW__
        m_soundRes.clear();
#endif // __WXMSW__
        m_useMemory = false;

        wxDELETE(m_sound);
        NotifyUsingFile(m_soundFile);
    }
#endif // wxUSE_FILEDLG
}

#ifdef __WXMSW__

void MyFrame::OnSelectResource(wxCommandEvent& WXUNUSED(event))
{
    m_soundRes = wxGetTextFromUser
                 (
                    "Enter resource name:",
                    "wxWidgets Sound Sample",
                    "FromResource",
                    this
                 );
    if ( m_soundRes.empty() )
        return;

    m_soundFile.clear();
    m_useMemory = false;

    wxDELETE(m_sound);

    NotifyUsingFile("Windows WAV resource");
}

#endif // __WXMSW__

void MyFrame::OnSelectMemory(wxCommandEvent& WXUNUSED(event))
{
    m_useMemory = true;

    NotifyUsingFile("embedded sound fragment");
}

void MyFrame::OnQuit(wxCommandEvent& WXUNUSED(event))
{
    // true is to force the frame to close
    Close(true);
}

void MyFrame::OnPlaySync(wxCommandEvent& WXUNUSED(event))
{
    wxBusyCursor busy;
    if ( !m_sound )
        m_sound = TryCreateSound();

    if (m_sound)
        m_sound->Play(wxSOUND_SYNC);
}

void MyFrame::OnPlayAsync(wxCommandEvent& WXUNUSED(event))
{
    wxBusyCursor busy;
    if ( !m_sound )
        m_sound = TryCreateSound();

    if (m_sound)
        m_sound->Play(wxSOUND_ASYNC);
}

void MyFrame::OnPlayAsyncOnStack(wxCommandEvent& WXUNUSED(event))
{
    wxBusyCursor busy;
    wxSound snd;
    CreateSound(snd);
    if (snd.IsOk())
        snd.Play(wxSOUND_ASYNC);
}

void MyFrame::OnPlayLoop(wxCommandEvent& WXUNUSED(event))
{
    wxBusyCursor busy;
    if ( !m_sound )
        m_sound = TryCreateSound();

    if (m_sound->IsOk())
        m_sound->Play(wxSOUND_ASYNC | wxSOUND_LOOP);
}

void MyFrame::OnPlayBell(wxCommandEvent& WXUNUSED(event))
{
    wxBell();
}

void MyFrame::OnAbout(wxCommandEvent& WXUNUSED(event))
{
    wxString msg;
    msg.Printf( "This is the About dialog of the sound sample.\n"
                "Welcome to %s", wxVERSION_STRING);

    wxMessageBox(msg, "About", wxOK | wxICON_INFORMATION, this);
}

void MyFrame::OnStop(wxCommandEvent& WXUNUSED(event))
{
    wxSound::Stop();
}
