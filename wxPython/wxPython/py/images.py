def getPyIcon():
    icon = wx.EmptyIcon()
    icon.CopyFromBitmap(getPyBitmap())
    return icon

def getPyBitmap():
    return wx.BitmapFromImage(getPyImage())

def getPyImage():
    stream = cStringIO.StringIO(getPyData())
    return wx.ImageFromStream(stream)

